		if(!alarmRing)
		{
			sprintf((char*)clockString,"    %02d:%02d:%02d        ",gTime.Hours, gTime.Minutes, gTime.Seconds);
			lcd_send_cmd(0x80);
			lcd_send_string(clockString);
			
			sprintf((char*)clockString,"   %02d-%02d-%2d         ",gDate.Date, gDate.Month, 2000 + gDate.Year);
			lcd_send_cmd(0xc0);
			lcd_send_string(clockString);

			HAL_Delay(200);
		}
		
		if(sAlarm.AlarmTime.Minutes == gTime.Minutes && sAlarm.AlarmTime.Hours == gTime.Hours && sAlarm.AlarmTime.Seconds == gTime.Seconds)
		{
			startForwardMotors();
			alarmRing = true;
			RingingAlarm();
			playGame();
		}
		else
			HAL_Delay(500);
		
		/* Display date Format: dd-mm-yy */ 
